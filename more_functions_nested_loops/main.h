#ifndef MAIN_H
#define MAIN_H

/**
 * _isupper - checks for an uppercase character
 * @c: character to check
 *
 * Return: 1 if uppercase, 0 otherwise
 */
int _isupper(int c);

/**
 * _isdigit - checks for a digit (0 through 9)
 * @c: character to check
 *
 * Return: 1 if digit, 0 otherwise
 */
int _isdigit(int c);

#endif /* MAIN_H */

